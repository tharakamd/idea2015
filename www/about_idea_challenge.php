                                <p align="justify" >University of Moratuwa is a leading university in Sri Lanka which currently consists of three faculties. Computer Science and Engineering Department that belongs to the Faculty of Engineering is one of the two departments in Sri Lanka along with University of Peradeniya to offer an engineering degree in computer science. Annually the department involves in organizing many events apart from the academic activities in order to improve the ICT knowledge and skills among the young students in Sri Lanka.</p>


                                <h6>What is the purpose of organizing this competition?</h6>
                                <p align="justify" >Most of the Sri Lankan students are interested in mobile application development but the main constraint for them is lack of knowledge and facilities. Only very few schools have well equipped ICT laboratories and resources persons. Main idea of organizing this event is to provide assistance  for students to learn the art of mobile application development and ultimately give them an opportunity to make use of what they have learnt and showcase their talents.</p>



